package com.example.pleaseadapterradisad;

public class MyListView {

    public String textView;
    public int image;

    public MyListView(String textView, int image) {
        this.textView = textView;
        this.image = image;
    }

    public String getTextView() {
        return textView;
    }

    public int getImage() {
        return image;
    }
}
